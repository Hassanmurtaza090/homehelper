// Helper functions
export const getServicesByCategory = (category: ServiceCategory): Service[] => {
  return mockServices.filter(service => service.category === category);
};

export const getProvidersByService = (serviceCategory: ServiceCategory): ServiceProvider[] => {
  return mockProviders.filter(provider => 
    provider.servicesOffered.includes(serviceCategory)
  );
};

export const getProviderById = (id: string): ServiceProvider | undefined => {
  return mockProviders.find(provider => provider.id === id);
};

export const getServiceById = (id: string): Service | undefined => {
  return mockServices.find(service => service.id === id);
};
